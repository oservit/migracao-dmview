INSERT INTO SNMP
(
HOSTNAME,
RETRIES,
TIMEOUT,
VERSION,
NOVACOLUNA,
ERP_CODE
)
SELECT
src.HOSTNAME,
src.RETRIES,
src.TIMEOUT,
src.VERSION,
src.NOVACOLUNA,
src.ID AS ERP_CODE
FROM CLARO.SNMP src

WHERE NOT EXISTS (
    SELECT 1 FROM SNMP dest WHERE dest.ERP_CODE = src.ID
);
COMMIT;